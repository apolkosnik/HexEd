void dnd_dest_setup( GtkWidget * dest){

	GtkTargetEntry text_targets[] = {
		{"text/uri-list", 0, TEXT_URI},
		{"text/html", 0, TEXT_HTML },
		{"STRING", 0, STRING}
	};

/* just for reference... 	
void gtk_drag_dest_set   (GtkWidget            *widget,
			  GtkDestDefaults       flags,
  		          const GtkTargetEntry *targets,
			  gint                  n_targets,
			  GdkDragAction         actions);	
*/ 
	
gtk_drag_dest_set( dest, 0, text_targets, 0, GDK_ACTION_COPY);

g_signal_connect(G_OBJECT(dest) , "drag_data_received" , G_CALLBACK(on_drag_data_received), NULL);

g_signal_connect(G_OBJECT(dest) , "drag_drop" , G_CALLBACK(on_drag_drop), NULL);

g_signal_connect(G_OBJECT(dest) , "drag_motion" , G_CALLBACK(on_drag_motion), NULL);

g_signal_connect(G_OBJECT(dest) , "drag_leave" , G_CALLBACK(on_drag_leave), NULL);

}

