/*
 * Print out a block as ascii text
 *
 * 
 * 
 * 
 */

gchar * asciiPrint (gchar *dest, gchar	*src, gsize buff_len)
	{

// k is used as an compensating extra offset into dest to smuggle in "\n" 
// also pointers on 64bit platforms are not of size int, they are long!
gulong i, j, k = 0 ;
guchar c;

	for(i = (gulong)src, j = 0; i < (gulong)src + buff_len; i++, j++) {
		c = src[j];
		if(g_ascii_isprint(c))
			dest[j+k] = c;
		else
			dest[j+k] = '.';
		
		if(j % LINE_LEN == LINE_LEN -1 && j != 0 && i< (gulong)src+ buff_len ) {
			k++;
			dest[j+k] = 0x0a;
		}
	}
	//k++;
	dest[j+k] = 0x00;
	return dest;
}

/*
 *  We need to convert the hex from the textview back into the binary
 * 
 * destination buffer (caller allocated, should not exceed the size of the source buffer),
   src - source buffer of ascii encoded hex values e.g. 41 44 41 4D and so on.. 
   buff_len - buffer length/size of the source buffer
 */
size_t unhex(gchar *dest, gchar *src, gsize buffl){

gchar n[3];
guchar k[8]; // extra safety for 64 bit
char *endptr;	
size_t i,j  = 0;
gboolean get_first = TRUE;

//g_print("%s\n", src);

// g_print("provided buff size:%d, computed: %d\n", buffl, strlen(src));
	n[3]= n[2] = n[1] = 0; // initialize our little string buffer
 	
   // so, let's track source index with j, and dest with i
   
	for(i = 0, j=0; j< buffl; j++){
		// skip the separators, and let's assume hex digits in pairs
		if (src[j] >= '0' && src[j] != '\t' ){
//			g_print("we got:%x,%x\n", src[j], src[j+1]);
			    // yes, in pairs of nibbles
			if(get_first){
				n[0] = src[j];
				get_first=FALSE;
			}else{
				n[1] = src[j];
				// converting using strtol, so the wheel was not redeveloped
				k[0]  =  strtol(n, &endptr, 16);
//				g_print("x: %x\n", k[0]);
				dest[i] = k[0];
//				g_print("wrote to dest\n");
				i++;
				get_first = TRUE;
			}
		}	
	}
//	g_print("unhex done, returning length: %d\n", i);
 return i;
	
}



